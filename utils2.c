#include "ft_ls.h"

int		list_len(t_file **list)
{
	t_file	*tmp;
	int		i;

	tmp = *list;
	i = 0;
	if (!tmp)
		return (0);
	while (tmp)
	{
		tmp = tmp->next;
		i++;
	}
	return (i);
}
